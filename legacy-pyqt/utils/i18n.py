REQUIRED_KEYS: Final = tuple(TRANSLATIONS[DEFAULT_LANGUAGE].keys())


def normalize_language(language: str | None) -> str:
    if language in SUPPORTED_LANGUAGES:
        return language
    return DEFAULT_LANGUAGE


def translate(language: str, key: str, **kwargs: object) -> str:
    language = normalize_language(language)
    value = TRANSLATIONS[language].get(
        key, TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    )
    if kwargs:
        return value.format(**kwargs)
    return value


def missing_translation_keys() -> dict[str, set[str]]:
    required = set(REQUIRED_KEYS)
    return {
        language: required - set(values.keys())
        for language, values in TRANSLATIONS.items()
        if required - set(values.keys())
    }
